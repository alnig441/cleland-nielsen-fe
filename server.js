/* configuration of express server to run production environment */

const path = require('path');
const express = require('express');
const webpack = require('webpack');
const webpackMiddleware = require('webpack-dev-middleware');
const webpackHotMiddleware = require('webpack-hot-middleware');
const config = require('./config/webpack.prod.js');
const configDev = require('./config/webpack.dev.js');

const isDeveloping = process.env.NODE_ENV !== 'production';
const url = isDeveloping ? "localhost" : "0.0.0.0";
const port = isDeveloping ? 3000 : process.env.PORT;
const app = express();

console.log('is prod? ', isDeveloping !== 'production', process.env.NODE_ENV);

if (isDeveloping) {
    console.log('dev mode');
    const compiler = webpack(config);
    const middleware = webpackMiddleware(compiler, {
        publicPath: config.output.publicPath,
        contentBase: 'src',
        stats: {
            colors: true,
            hash: false,
            timings: true,
            chunks: false,
            chunkModules: false,
            modules: false
        }
    });

    app.use(middleware);
    app.use(webpackHotMiddleware(compiler));
    app.get('*', function response(req, res) {
        res.write(middleware.fileSystem.readFileSync(path.join(__dirname, 'dist/index.html')));
        res.end();
    });
} else {
    console.log('prod mode');
    app.use(express.static(__dirname + '/dist'));
    app.get('*', function response(req, res) {
        res.sendFile(path.join(__dirname, 'dist/index.html'));
    });
}

app.listen(port, url, function onStart(err) {
    if (err) {
        console.log(err);
    }
    console.info('==> ðŸŒŽ Listening on port %s. Open up http://0.0.0.0:%s/ in your browser.', port, port);
});
